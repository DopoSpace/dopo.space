/**
 * Infer gender from a first name.
 *
 * The function attempts to determine gender using:
 * 1. Exact match in the database
 * 2. First part of compound names (e.g., "Maria Teresa" → "Maria")
 * 3. Common feminine suffixes (-ella, -etta, -ina, -ilde, -issa)
 * 4. Common masculine suffixes (-iano, -ello, -ardo, -andro, -enzo)
 *
 * @param firstName - The first name to analyze
 * @returns 'M' | 'F' if gender can be determined, null otherwise
 */
export function inferGenderFromName(firstName: string): 'M' | 'F' | null {
	if (!firstName) return null;

	const normalized = firstName.trim().toUpperCase();
	if (!normalized) return null;

	// 1. Direct lookup in database
	if (ITALIAN_NAMES[normalized]) {
		return ITALIAN_NAMES[normalized];
	}

	// 2. Try first part of compound names (e.g., "Maria Teresa" → "Maria")
	const firstPart = normalized.split(/[\s-]/)[0];
	if (firstPart && firstPart !== normalized && ITALIAN_NAMES[firstPart]) {
		return ITALIAN_NAMES[firstPart];
	}

	// 3. Heuristic based on common feminine suffixes
	// These are very reliable in Italian and most European languages:
	if (
		normalized.endsWith('ELLA') || // Antonella, Raffaella, Donatella
		normalized.endsWith('ETTA') || // Giulietta, Nicoletta, Elisabetta
		normalized.endsWith('INA') || // Valentina, Cristina, Martina
		normalized.endsWith('ILDE') || // Matilde, Brunilde, Clotilde
		normalized.endsWith('ISSA') || // Clarissa, Melissa, Larissa
		normalized.endsWith('OVNA') || // Russian patronymic (female)
		normalized.endsWith('EVNA') // Russian patronymic (female)
	) {
		return 'F';
	}

	// 4. Heuristic based on common masculine suffixes
	if (
		normalized.endsWith('IANO') || // Emiliano, Sebastiano, Giuliano
		normalized.endsWith('ELLO') || // Marcello, Raffaello
		normalized.endsWith('ARDO') || // Leonardo, Edoardo, Riccardo
		normalized.endsWith('ANDRO') || // Alessandro, Leandro, Sandro
		normalized.endsWith('ENZO') || // Vincenzo, Lorenzo, Renzo
		normalized.endsWith('OVIC') || // Slavic male patronymic
		normalized.endsWith('OVICH') // Slavic male patronymic
	) {
		return 'M';
	}

	// Cannot determine gender with confidence
	return null;
}
